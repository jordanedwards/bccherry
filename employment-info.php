		<h4>TYPICAL HOURS OF WORK:</h4>
	<p>Picking crews generally work from sunrise until noon, depending on the heat.  Packinghouse shifts generally start an hour or two later and run for 8 or 9 hours. Some orchards may run two shifts or picking crews come in after picking to do a short shift in the afternoon.<br>
	<h4>WAGES:</p>
	<p>Wages vary from orchard to orchard but there is a minimum picking piecework rate of (2013) $2.24 per pail, including holiday pay.  Packinghouse wages usually depend on experience and skill, most paying well above minimum wage for hourly jobs.  Some packinghouses offer piecework pay systems for sorting.</p>
	<p>Information about employment standards, rates, and regulations is available on the <a href="http://www.labour.gov.bc.ca/esb/agriculture/welcome.htm" target="_blank">BC Ministry’s website for Agriculture Workers and Employers</a></p>
	<h4>WHO’S HIRING?</h4>
	<p>Grower members of the BC Cherry Assocation are listed in the Who’s Hiring link. Most keep “lists” from year to year so be sure to make your contacts early with the individual orchardist. A few of the larger farms have their own linked websites to provide more details as to what to expect.  Many growers have on-site camping facilities for their workers.</p>
